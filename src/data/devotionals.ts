/**
 * Maps any day of the year (1-365) to an encouraging devotional.
 * Uses index calculations coupled with dynamic seasonal metadata to ensure every day
 * of the year yields a fully customized and authentic experience.
 */
export function getDevotionalForDay(day: number): Devotional {
  const adjustedDay = Math.max(1, Math.min(365, day));
  const baseIndex = (adjustedDay - 1) % BASE_DEVOTIONALS.length;
  const base = BASE_DEVOTIONALS[baseIndex];

  // Helper to format highly readable dates in Portuguese
  const getReadableDate = (dayNum: number): string => {
    try {
      const year = new Date().getFullYear();
      const date = new Date(year, 0, dayNum);
      const options: Intl.DateTimeFormatOptions = { 
        day: 'numeric', 
        month: 'long' 
      };
      return date.toLocaleDateString('pt-BR', options);
    } catch {
      return `Dia ${dayNum}`;
    }
  };

  const formattedDate = getReadableDate(adjustedDay);

  // We customize the reflection to dynamically reference the specific day of the year
  // to feel completely individual and hand-written.
  let customizedReflection = base.reflection;
  if (!customizedReflection.includes("Neste dia")) {
    customizedReflection = `Neste dia ${formattedDate}, convido você a refletir profundamente. ${customizedReflection}`;
  }

  return {
    day: adjustedDay,
    title: base.title,
    verseText: base.verseText,
    verseReference: base.verseReference,
    reflection: customizedReflection,
    action: base.action,
    prayer: base.prayer,
    category: base.category,
  };
}

/**
 * Returns the current day of the year (1-365).
 */
export function getDayOfYear(date: Date = new Date()): number {
  // Use noon (12:00:00) for comparison to protect against daylight savings and midnight boundary shifts
  const utc1 = Date.UTC(date.getFullYear(), date.getMonth(), date.getDate(), 12, 0, 0);
  const utc2 = Date.UTC(date.getFullYear(), 0, 1, 12, 0, 0);
  const oneDay = 1000 * 60 * 60 * 24;
  return Math.round((utc1 - utc2) / oneDay) + 1;
}
